a = [[0]*3 for _ in range(3)]
print(a)  # [[0, 0, 0], [0, 0, 0], [0, 0, 0]]

# 추후 알고리즘 문제 풀 때  행, 열로 생각해서 문제 풀기
a[1][1] = 3
a[2][2] = 5
print(a)  # [[0, 3, 0], [0, 0, 0], [0, 0, 0]]

for x in a:
    print(x)
'''
[0, 0, 0]
[0, 3, 0]
[0, 0, 5]
'''

for x in a:
    for y in x:
        print(y, end=" ")
    print()
'''
0 0 0 
0 3 0 
0 0 5 
'''